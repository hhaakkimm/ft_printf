#include <string.h>

char	*ft_compare(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1 && s2 && s1[i] == s2[i])
		i++;
	if (s2[i] == '\0')
		return (s1 + i);
	return (NULL);
}
